						this.$utils.msg(uni.getStorageSync('tips'));
						return false;
					} else {
                //跨表计算
						if(this.ruleForm.id){
							await this.$api.update(`fuwuxiangmu`, this.ruleForm);
						}else{
							await this.$api.add(`fuwuxiangmu`, this.ruleForm);
						}
						this.$utils.msgBack('提交成功');
					}
				} else {
                //跨表计算
					if(this.ruleForm.id){
						await this.$api.update(`fuwuxiangmu`, this.ruleForm);
					}else{
						await this.$api.add(`fuwuxiangmu`, this.ruleForm);
					}
					this.$utils.msgBack('提交成功');
				}
