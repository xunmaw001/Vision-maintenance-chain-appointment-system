			this.xiangmuleixingOptions = res.data;

			// 如果有登陆，获取登陆后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`fuwuxiangmu`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			this.cross = options.cross;
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='xiangmubianhao'){
					this.ruleForm.xiangmubianhao = obj[o];
					this.ro.xiangmubianhao = true;
					continue;
					}
					if(o=='xiangmumingcheng'){
					this.ruleForm.xiangmumingcheng = obj[o];
					this.ro.xiangmumingcheng = true;
					continue;
					}
					if(o=='xiangmuleixing'){
					this.ruleForm.xiangmuleixing = obj[o];
					this.ro.xiangmuleixing = true;
					continue;
					}
					if(o=='jiage'){
					this.ruleForm.jiage = obj[o];
					this.ro.jiage = true;
					continue;
					}
					if(o=='fengmian'){
					this.ruleForm.fengmian = obj[o];
					this.ro.fengmian = true;
					continue;
					}
					if(o=='xiangxijieshao'){
					this.ruleForm.xiangxijieshao = obj[o];
					this.ro.xiangxijieshao = true;
